    private void btn_registerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_registerMouseClicked
        try{
            String fname = txt_fname.getText();
            String lname = txt_lname.getText();
            Date birthDay = dateOfBirth.getDate();
            String gender = buttonGroup1.getSelection().getActionCommand();
            String address = txt_address.getText();
            String email = txt_email.getText();
            int mobile = Integer.parseInt(txt_mobile.getText());
            int homePhone = Integer.parseInt(txt_homePhone.getText());
            String parentName = txt_pName.getText();
            String nic = txt_pnic.getText();
            int parentMobile = Integer.parseInt(txt_pMobile.getText());

            if(fname.equals("")|| lname.equals("") || birthDay == null || address.equals("") || parentName.equals("null")){
                JOptionPane.showMessageDialog(null, "Please fill all required fields" , "Registration", 3);
            }
            else{
                Student student = new Student(fname, lname, birthDay, gender, address, email, mobile, homePhone, parentName, nic, parentMobile);

                student.insertStudent(student);

                clearFields();
                
                populateCBox();

            }
        }
        catch(NumberFormatException npe){
            JOptionPane.showMessageDialog(null, "Please fill all required fields" , "Registration", 3);
        }catch(NullPointerException ne){
            JOptionPane.showMessageDialog(null, "Please fill all required fields" , "Registration", 3);
        }
      
    }//GEN-LAST:event_btn_registerMouseClicked


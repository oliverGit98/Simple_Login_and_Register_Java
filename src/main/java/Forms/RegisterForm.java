    private void btn_deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_deleteMouseClicked
        
        try{
            int id = Integer.parseInt(jComboBox1.getSelectedItem().toString());
        
            String fname = txt_fname.getText();
            Student student = new Student();

            if(fname.equals("")){
                JOptionPane.showMessageDialog(null, "Please Select the student (press enter on regno)" , "Registration", 3);
            }
            else{
                int reply = JOptionPane.showConfirmDialog(null, "Do you want to delete "+id+" - "+fname, "Delete Record", JOptionPane.YES_NO_OPTION);
                if (reply == JOptionPane.YES_OPTION) {
                    student.deleteStudent(id);

                    clearFields();

                    populateCBox();

                } else {
                    JOptionPane.showMessageDialog(null, "Ok");
                    clearFields();
                }
            }
        }catch(NullPointerException npe){
            JOptionPane.showMessageDialog(null, "Please select the user" , "Delete user", 3);
        }
        
       
    }//GEN-LAST:event_btn_deleteMouseClicked


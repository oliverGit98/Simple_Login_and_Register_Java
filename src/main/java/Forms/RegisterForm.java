    private void jComboBox1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jComboBox1KeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            int id = Integer.parseInt(jComboBox1.getSelectedItem().toString());
            
            Student student = new Student();
            Student stdata = student.getStudent(id);
            
            txt_fname.setText(stdata.getFname());
            txt_lname.setText(stdata.getLname());
            dateOfBirth.setDate(stdata.getDateOfBirth());

            String gender = stdata.getGender();

            if(gender.equals("Male")){
                maleRadioBtn.setSelected(true);
            }else if(gender.equals("Female")){
                femaleRadioBtn.setSelected(true);
            }else{
                buttonGroup1.clearSelection();
            }

            txt_address.setText(stdata.getAddress());
            txt_email.setText(stdata.getEmail());
            txt_mobile.setText(String.valueOf(stdata.getMobileNo()));
            txt_homePhone.setText(String.valueOf(stdata.getHomePhone()));
            txt_pName.setText(stdata.getParentName());
            txt_pnic.setText(stdata.getParentNic());
            txt_pMobile.setText(String.valueOf(stdata.getParentMobile()));
//         
            
        }
    }//GEN-LAST:event_jComboBox1KeyPressed

